package Strings;

public class containIndex {
    public static void main(String[] args) {
        String c = "Priyanshu Negi";
        System.out.println(c.contains("yan"));
        System.out.println(c.contains("walla"));

        System.out.println(c.startsWith("Pri"));
        System.out.println(c.endsWith("Negi"));
    }
}
