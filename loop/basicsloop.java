package loop;

import java.util.Scanner;

public class basicsloop {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        for (int i = 1; i <= n; i++) {
            System.out.println("Hello world");
            sc.close();
        }
    }
}
